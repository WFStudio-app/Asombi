chmod +x "$INSTALL_DIR/Asombi/bin/wiz"
chmod +x "$INSTALL_DIR/Asombi/bin/os"

echo ""
echo "[‚úď] Wizzor installed successfully!"
echo "[‚úď] Run: wiz help"
